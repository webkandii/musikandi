<div class="three-column-23">
<p>Music Jobs is the go-to site for music recruitment. OK, realistically, if you work in a venue, or label whatever, you'll get sent a steady stream of CVs which you can flick through every time you need an engineer. But if you want to spread the net a bit further, or are starting up and don't have a load of CVs to hand, then it's worth advertising your vacancies on Music Jobs.
It's also great if you are looking for music industry related work, whether you're a musician, PR, events manager etc.
To get full access to the jobs, you need to pay a small monthly fee but it's good value and worth it.
The most impressive thing about Music Jobs is that they're human. If you have a question or complaint, they'll always get back. They don't live in a corporate bubble. They're real people working out of probably quite a small office somewhere in London. A good company to deal with.</p>
